A = [1, -3, 5, -2, 7, -4, 6, -1]

# Находим индекс первого вхождения максимального и минимального элемента
max_idx = A.index(max(A))   # индекс первого максимума
min_idx = A.index(min(A))   # индекс первого минимума

# Определяем границы среза "между" этими индексами.
start = min(max_idx, min_idx) + 1 # Берём меньший индекс +1 как старт (не включаем сам край) и больший индекс как конец
end = max(max_idx, min_idx)

# Делаем срез.
middle_segment = A[start:end]

# Суммируем только отрицательные элементы в этом срезе
sum_neg = sum(x for x in middle_segment if x < 0)

# Выводим результаты для наглядности
print(f"Список: {A}")
print(f"Индекс max: {max_idx}, значение: {A[max_idx]}")
print(f"Индекс min: {min_idx}, значение: {A[min_idx]}")
print(f"Элементы между ними (не включая max/min): {middle_segment}")
print(f"Сумма отрицательных элементов между max и min: {sum_neg}")